package project.game;

public class Line {
    public int i1;
    public int j1;
    public int i2;
    public int j2;
    public int player;


    public Line(int i1, int j1, int i2, int j2, int player) {
        this.i1 = i1;
        this.j1 = j1;
        this.i2 = i2;
        this.j2 = j2;
        this.player = player;
    }
}
